package infrastructure.performance.entity

/**
 * Уровни значимости для анализа производительности.
 * Определяют строгость критериев обнаружения аномалий.
 */
enum class SignificanceLevel {
    /**
     * Аномалии не обнаружены ни на одном уровне значимости
     */
    NOT_SIGNIFICANT,

    /**
     * Менее строгий уровень для поиска потенциально интересных сидов во время эволюции
     */
    SEED_EVOLUTION,

    /**
     * Строгий уровень для финальных отчетов и подтверждения аномалий
     */
    REPORTING,
}
