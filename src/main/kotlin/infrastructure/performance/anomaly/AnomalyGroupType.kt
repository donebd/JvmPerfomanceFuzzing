package infrastructure.performance.anomaly

/**
 * Типы аномалий, которые могут быть обнаружены
 * при сравнительном тестировании различных JVM.
 */
enum class AnomalyGroupType {
    /**
     * Значительные различия во времени выполнения между JVM.
     */
    TIME,

    /**
     * Существенные различия в использовании памяти между JVM.
     */
    MEMORY,

    /**
     * Выполнение завершилось по тайм-ауту на одной JVM и успешно на другой.
     */
    TIMEOUT,

    /**
     * Выполнение завершилось с ошибкой на одной JVM и успешно на другой.
     */
    ERROR,

    /**
     * Различия в характеристиках JIT-компиляции (например, количество компиляций,
     * оптимизации, инлайнинг).
     */
    JIT;
}